    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        try {
            int response = JOptionPane.showConfirmDialog(null, "Â¿EstÃ¡s seguro de que quieres editar este chat?", "Confirmar", JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE);
            if (response == JOptionPane.YES_OPTION) {
                Icon icon = jButton1.getIcon();
                byte[] profileImageBytes = null;

                if (icon instanceof ImageIcon) {
                    BufferedImage bufferedImage = new BufferedImage(icon.getIconWidth(), icon.getIconHeight(), BufferedImage.TYPE_INT_ARGB);
                    Graphics g = bufferedImage.createGraphics();
                    icon.paintIcon(null, g, 0, 0);
                    g.dispose();

                    ByteArrayOutputStream baos = new ByteArrayOutputStream();
                    ImageIO.write(bufferedImage, "png", baos);
                    baos.flush();
                    profileImageBytes = baos.toByteArray();
                    baos.close();
                } else {
                    throw new IllegalArgumentException("Icono del botÃ³n no es un ImageIcon");
                }

                ChatDTO chatUpdate=chat;
                chatUpdate.setChatImage(profileImageBytes);
                chatUpdate.setChatName(txtName.getText());
                busBO.updateChat(chatUpdate);
                this.dispose();
            }
        } catch (ExceptionService ex) {
            Logger.getLogger(EditChat.class.getName()).log(Level.SEVERE, null, ex);
        } catch (IOException ex) {
            Logger.getLogger(EditChat.class.getName()).log(Level.SEVERE, null, ex);
        }

    }//GEN-LAST:event_jButton3ActionPerformed


